/**
 * dcate
 * A list consisting of elements of A followed by the
 * elements of B. May modify items of A.
 * Don't use 'new'
 * @param {List} A
 * @param {List} B
 * @returns {List}
 */
function dcate(A, B) {
	/** Fill in here **/
	let nextTail = A.tail

	while (nextTail) {
		if (!nextTail.tail) break
		nextTail = nextTail.tail
	}

	nextTail.tail = B
	return A
}

