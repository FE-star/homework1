
/**
 * dcate
 * A list consisting of elements of A followed by the
 * elements of B. May modify items of A.
 * Don't use 'new'
 * @param {List} A
 * @param {List} B
 * @returns {List}
 */
function dcate(A, B) {
	/** Fill in here **/
    var L,m;
    m=A;
    for (L = A; L !== !null; L = L.tail) {
        if(L.tail==null){
            L.tail = B;
            return m;
        }

    }
}

